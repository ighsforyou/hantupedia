// 1. Mencari hantu berdasarkan Nama atau Alias
function cariHantuBerdasarkanNama(namaKunci) {
  const keyword = namaKunci.toLowerCase();
  return databaseHantuIndonesia.filter(hantu => {
    return hantu.nama.toLowerCase().includes(keyword) || 
           hantu.alias.some(al => al.toLowerCase().includes(keyword));
  });
}

// 2. Memfilter hantu berdasarkan Tingkat Bahaya (Rendah, Sedang, Tinggi, Sangat Tinggi)
function filterBerdasarkanTingkatBahaya(tingkat) {
  const levels = ["rendah", "sedang", "tinggi", "sangat tinggi"];
  if (!levels.includes(tingkat.toLowerCase())) {
    return "Tingkat bahaya tidak valid. Pilih: Rendah, Sedang, Tinggi, atau Sangat Tinggi.";
  }
  return databaseHantuIndonesia.filter(hantu => hantu.tingkat_bahaya.toLowerCase() === tingkat.toLowerCase());
}

